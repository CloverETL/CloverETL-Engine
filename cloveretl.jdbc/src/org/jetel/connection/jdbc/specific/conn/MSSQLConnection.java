package org.jetel.connection.jdbc.specific.conn;

import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;

import org.jetel.connection.jdbc.DBConnection;
import org.jetel.connection.jdbc.specific.JdbcSpecific.AutoGeneratedKeysType;
import org.jetel.connection.jdbc.specific.JdbcSpecific.OperationType;
import org.jetel.exception.JetelException;

/**
 * Adapter for MS SQL java.sql.Connection.
 * 
 * NOTE: this connection is used by MS SQL and Informix specifics.
 * 
 * @author Martin Zatopek (martin.zatopek@javlinconsulting.cz)
 *         (c) Javlin Consulting (www.javlinconsulting.cz)
 *
 * @created May 29, 2008
 */
public class MSSQLConnection extends DefaultConnection {

	public MSSQLConnection(DBConnection dbConnection, OperationType operationType) throws JetelException {
		super(dbConnection, operationType, AutoGeneratedKeysType.SINGLE);
	}
	
	/* (non-Javadoc)
	 * @see org.jetel.connection.jdbc.specific.conn.DefaultConnection#createStatement()
	 */
	@Override
	public Statement createStatement() throws SQLException {
		Statement statement;
		
		switch (operationType) {
		case READ:
			statement = connection.createStatement(ResultSet.TYPE_FORWARD_ONLY, ResultSet.CONCUR_READ_ONLY);
			break;
		default:
			statement = connection.createStatement();
		}
		
		return optimizeStatement(statement);
	}
	
	/* (non-Javadoc)
	 * @see org.jetel.connection.jdbc.specific.conn.DefaultConnection#prepareStatement(java.lang.String)
	 */
	@Override
	public PreparedStatement prepareStatement(String sql) throws SQLException {
		switch (operationType) {
		case READ:
			return connection.prepareStatement(sql, ResultSet.TYPE_FORWARD_ONLY, ResultSet.CONCUR_READ_ONLY);
		default:
			return connection.prepareStatement(sql);
		}
	}

	/* (non-Javadoc) We had to ommit holdability settings.
	 * @see org.jetel.connection.jdbc.specific.conn.DefaultConnection#optimizeConnection(org.jetel.connection.jdbc.specific.JdbcSpecific.OperationType)
	 */
	@Override
	protected void optimizeConnection(OperationType operationType) {
		switch (operationType) {
		case READ:
			try {
				connection.setAutoCommit(false);
				connection.setReadOnly(true);
				connection.setTransactionIsolation(Connection.TRANSACTION_READ_UNCOMMITTED);
			} catch (SQLException ex) {
				logger.warn("Optimizing connection failed: " + ex.getMessage());
				logger.warn("Try to use another jdbc specific");
			} catch (UnsupportedOperationException ex) {
				logger.warn("Optimizing connection failed: " + ex.getMessage());
				logger.warn("Try to use another jdbc specific");
			}
			break;
		case WRITE:
		case CALL:
			try {
				connection.setAutoCommit(false);
				connection.setReadOnly(false);
				connection.setTransactionIsolation(Connection.TRANSACTION_READ_UNCOMMITTED);
			} catch (SQLException ex) {
				logger.warn("Optimizing connection failed: " + ex.getMessage());
				logger.warn("Try to use another jdbc specific");
			} catch (UnsupportedOperationException ex) {
				logger.warn("Optimizing connection failed: " + ex.getMessage());
				logger.warn("Try to use another jdbc specific");
			}
			break;

		case TRANSACTION:
			try {
				connection.setAutoCommit(true);
				connection.setReadOnly(false);
				connection.setTransactionIsolation(Connection.TRANSACTION_READ_UNCOMMITTED);
			} catch (SQLException ex) {
				logger.warn("Optimizing connection failed: " + ex.getMessage());
				logger.warn("Try to use another jdbc specific");
			} catch (UnsupportedOperationException ex) {
				logger.warn("Optimizing connection failed: " + ex.getMessage());
				logger.warn("Try to use another jdbc specific");
			}
			break;
		}
	}

}
