package org.jetel.connection.jdbc.config;

import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;
import java.sql.Types;

import org.jetel.metadata.DataFieldMetadata;


/**
 * @author Agata Vackova (agata.vackova@javlinconsulting.cz) ; 
 * (c) JavlinConsulting s.r.o.
 *  www.javlinconsulting.cz
 *
 * @since Apr 9, 2008
 *
 */

public class JdbcMssqlConfig extends JdbcBaseConfig {

	private static JdbcMssqlConfig instance=new JdbcMssqlConfig(AutoGeneratedKeysType.SINGLE);

	/**
	 * @param autoKeyType
	 */
	private JdbcMssqlConfig(AutoGeneratedKeysType autoKeyType) {
		super(autoKeyType);
	}

	public static JdbcMssqlConfig getInstance(){
		return instance;
	}
	
	@Override
	public String getTargetDBName() {
		return "SQLSERVER";
	}
	
	public Statement createStatement(Connection con, OperationType operType) throws SQLException{
		switch (operType) {
			case READ:
					return con.createStatement(ResultSet.TYPE_FORWARD_ONLY, ResultSet.CONCUR_READ_ONLY);
			default:
				return con.createStatement();
		}
	}

	@Override
	public PreparedStatement createPreparedStatement(Connection connection,
			String sqlQuery, String[] columns) throws SQLException {
		if (columns != null) {
			return connection.prepareStatement(sqlQuery, Statement.RETURN_GENERATED_KEYS);
		}
		logger.warn("Columns are null");
		logger.info("Getting generated keys switched off !");
		return super.createPreparedStatement(connection, sqlQuery, columns);
	}
	
	public char sqlType2jetel(int sqlType) {
		switch (sqlType) {
			case Types.INTEGER:
			case Types.SMALLINT:
			case Types.TINYINT:
			    return DataFieldMetadata.INTEGER_FIELD;
			//-------------------
			case Types.BIGINT:
			    return DataFieldMetadata.LONG_FIELD;
			//-------------------
			case Types.DECIMAL:
			case Types.NUMERIC:
				return DataFieldMetadata.DECIMAL_FIELD;
			case Types.DOUBLE:
			case Types.FLOAT:
			case Types.REAL:
				return DataFieldMetadata.NUMERIC_FIELD;
			//------------------
			case Types.CHAR:
			case Types.LONGVARCHAR:
			case Types.VARCHAR:
			case Types.CLOB:
				return DataFieldMetadata.STRING_FIELD;
			//------------------
			case Types.DATE:
			case Types.TIME:
			case Types.TIMESTAMP:
				return DataFieldMetadata.DATE_FIELD;
            //-----------------
            case Types.BINARY:
            case Types.VARBINARY:
            case Types.LONGVARBINARY:
            case Types.BLOB:
			case Types.OTHER:
               return DataFieldMetadata.BYTE_FIELD;
			//-----------------
			case Types.BOOLEAN:
			case Types.BIT:
				return DataFieldMetadata.BOOLEAN_FIELD;
			default:
				throw new IllegalArgumentException("Can't handle JDBC.Type :"+sqlType);
		}
	}

}
