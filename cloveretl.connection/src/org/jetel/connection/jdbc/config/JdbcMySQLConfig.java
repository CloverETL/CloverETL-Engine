package org.jetel.connection.jdbc.config;

import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;

/**
 * @author dadik
 *
 */
public class JdbcMySQLConfig extends JdbcBaseConfig {

	private static JdbcMySQLConfig instance=new JdbcMySQLConfig(AutoGeneratedKeysType.SINGLE);
	
	protected JdbcMySQLConfig(){
		super();
	}

	private JdbcMySQLConfig(AutoGeneratedKeysType single) {
		super(single);
	}

	public static JdbcMySQLConfig getInstance(){
		return instance;
	}

	public String getTargetDBName(){
		return "MYSQL";
	}
	
	public int getTargetDBMajorVersion(){
		return -1;
	}
	
	
	public Statement createStatement(Connection con, OperationType operType) throws SQLException{
		Statement stm=super.createStatement(con,operType);
		switch (operType) {
			case READ:
					stm.setFetchSize(Integer.MIN_VALUE);
				break;
		}
		return stm;
	}
	
	@Override
	public PreparedStatement createPreparedStatement(Connection connection, String sqlQuery, String[] columns) throws SQLException {
		if (columns != null) {
			return connection.prepareStatement(sqlQuery, Statement.RETURN_GENERATED_KEYS);
		}
		logger.warn("Columns are null");
		logger.info("Getting generated keys switched off !");
		return super.createPreparedStatement(connection, sqlQuery, columns);
	}

	public void optimizeResultSet(ResultSet res,OperationType operType){
		switch(operType){
		case READ:
			try{
				res.setFetchDirection(ResultSet.FETCH_FORWARD);
				res.setFetchSize(Integer.MIN_VALUE);
			}catch(SQLException ex){
				//TODO: for now, do nothing
			}
		}
	}
	
}
